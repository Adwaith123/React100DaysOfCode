function App() {
  return (
    <div className="container">
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}

function Header() {
  return (
    <header className="header">
      <h1>Fast React Pizza Co.</h1>
    </header>
  );
}
function Menu() {
  const pizzas = pizzaData;

  // const pizzas = [];
  const numPizzas = pizzas.length;
  return (
    <main className="menu">
      <h2>Our Menu</h2>
      {/* The below situation depicts the need for react frangment , where we need send two separate jsx elements without a common parent elemnt
       */}
      {numPizzas > 0 ? (
        <React.Fragment>
          {/* In a nutshell react fragments allow to have more than one element in a piece of jsx*/}
          {/*or otherwise use <>..</> */} {/*Recat Fragements */}
          <p>
            Authentic Italian cuisine. 6 creative dishes to choose from. All
            from our stone oven, all organic, all delicious.
          </p>
          <ul className="pizzas">
            {pizzas.map((pizza) => (
              <Pizza pizzaObj={pizza} key={pizza.name} />
            ))}
          </ul>
        </React.Fragment>
      ) : (
        <p>We're Still working on Our Menu{/*null can also be used */}</p>
      )}
    </main>
  );
}

// Destructuring
function Pizza({ pizzaObj }) {
  // console.log(pizzaObj);
  // Best use case of Ternary Operator
  return (
    // Note the special usage of conditional classes below
    <li className={`pizza ${pizzaObj.soldOut ? "sold-out" : ""}`}>
      {/* Here ${} is an expresion which produces a value */}
      <img src={pizzaObj.photoName} alt={pizzaObj.name} />
      <div>
        <h3>{pizzaObj.name}</h3>
        <p>{pizzaObj.ingredients}</p>
        {/* ---Setting Text Conditionally 2 ways  ---------*/}
        {/* {pizzaObj.soldOut ? (
          <span>SOLD OUT</span>
        ) : (
          <span>{pizzaObj.price}</span>
        )} */}
        <span>{pizzaObj.soldOut ? "SOLD OUT" : pizzaObj.price}</span>
      </div>
    </li>
  );
}

function Footer() {
  const hour = new Date().getHours();
  const openHour = 12;
  const closeHour = 22;
  const isOpen = hour >= openHour && hour <= closeHour; // true or false

  // if (!isOpen) return <p>CLOSED</p>; This is not we want

  return (
    <footer className="footer">
      {isOpen ? (
        <Order closeHour={closeHour} openHour={openHour} />
      ) : (
        <p>
          We're Happy to welcome you between {openHour}:00 and {closeHour}:00
        </p>
      )}
    </footer>
  );
}

function Order({ closeHour, openHour }) {
  return (
    <div className="order">
      <p>
        We're Open from {openHour}:00 until {closeHour}:00. Come vist us or
        Order Online
      </p>
      <button className="btn">Order</button>
    </div>
  );
}

