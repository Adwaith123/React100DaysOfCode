function App() {
  return (
    <div className="container">
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}

function Header() {
  return (
    <header className="header">
      <h1>Fast React Pizza Co.</h1>
    </header>
  );
}
function Menu() {
  const pizzas = pizzaData;

  const numPizzas = pizzas.length;
  return (
    <main className="menu">
      <h2>Our Menu</h2>
      {numPizzas > 0 ? (
        <ul className="pizzas">
          {pizzas.map((pizza) => (
            // The reason why we created map i sthat we need to render list inside <ul> that means  a set of jsx for that wee need nw array</ul>
            <Pizza pizzaObj={pizza} key={pizza.name} />
          ))}
        </ul>
      ) : (
        <p>We're Still working on Our Menu{/*null can also be used */}</p>
      )}
    </main>
  );
}

// Destructuring
function Pizza({ pizzaObj }) {
  // console.log(pizzaObj);
  // Best use case of Ternary Operator
  return pizzaObj.soldOut ? null : (
    <li className="pizza">
      <img src={pizzaObj.photoName} alt={pizzaObj.name} />
      <div>
        <h3>{pizzaObj.name}</h3>
        <p>{pizzaObj.ingredients}</p>
        <span>{pizzaObj.price}</span>{" "}
      </div>
    </li>
  );
}

function Footer() {
  const hour = new Date().getHours();
  const openHour = 12;
  const closeHour = 22;
  const isOpen = hour >= openHour && hour <= closeHour; // true or false

  // if (!isOpen) return <p>CLOSED</p>; This is not we want

  return (
    <footer className="footer">
      {isOpen ? (
        <Order closeHour={closeHour} openHour={openHour} />
      ) : (
        <p>
          We're Happy to welcome you between {openHour}:00 and {closeHour}:00
        </p>
      )}
    </footer>
  );
}

//Extracting Order into a new component
function Order({ closeHour, openHour }) {
  // Only with curly braces you are destructuring
  return (
    <div className="order">
      <p>
        We're Open from {openHour}:00 until {closeHour}:00. Come vist us or
        Order Online
      </p>
      <button className="btn">Order</button>
    </div>
  );
}

const root = ReactDOM.createRoot(document.getElementById("root"));
